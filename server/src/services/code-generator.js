/**
 * Converts block programs (JSON) to MicroPython code for mBot2
 * This is used when generating code to upload to the robot
 */

function escapePyString(value) {
  return String(value ?? '').replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n').replace(/\r/g, '\\r');
}

function sanitizeIdentifier(name) {
  const s = String(name ?? 'my_var').replace(/[^a-zA-Z0-9_]/g, '_');
  return /^[a-zA-Z_]/.test(s) ? s : '_' + s;
}

const ALLOWED_OPS = new Set(['>', '<', '>=', '<=', '==', '!=', '+', '-', '*', '/']);

function sanitizeOp(op) {
  return ALLOWED_OPS.has(op) ? op : '==';
}

const COMPARE_OPS = new Set(['>', '<', '>=', '<=', '==', '!=']);
function sanitizeCompareOp(op) {
  return COMPARE_OPS.has(op) ? op : '==';
}

const SOUND_ALIASES = {
  level_up: 'level-up', low_energy: 'low-energy', prompt_tone: 'prompt-tone',
  metal_clash: 'metal-clash', glass_clink: 'glass-clink',
  running_water: 'running-water', wood_hit: 'wood-hit',
  beep: 'beeps', spring: 'sprint', scared: 'warning',
  power_up: 'level-up', powerup: 'level-up', 'power-up': 'level-up',
  power_down: 'low-energy', powerdown: 'low-energy', 'power-down': 'low-energy',
  alert: 'warning', happy: 'yeah', excited: 'yeah', applause: 'yeah',
  fail: 'wrong', win: 'right', success: 'right', error: 'wrong',
  siren: 'warning',
};
function canonicalSound(name) {
  const key = String(name || 'laugh').trim().toLowerCase();
  return SOUND_ALIASES[key] || key;
}


/**
 * Produce a Python expression for an Operator-block operand.
 * - numbers → numeric literal
 * - identifier-looking strings ("x", "my_var") → bare variable reference
 * - anything else → quoted string literal
 */
function valueExpr(v) {
  if (v === undefined || v === null || v === '') return '0';
  if (typeof v === 'number') return String(v);
  if (typeof v === 'boolean') return v ? '1' : '0';
  const s = String(v);
  // Numeric literal?
  if (/^-?\d+(\.\d+)?$/.test(s)) return s;
  // Plain identifier? Treat as variable reference.
  if (/^[a-zA-Z_][a-zA-Z0-9_]*$/.test(s)) return sanitizeIdentifier(s);
  // Otherwise quote as a string literal.
  return `"${escapePyString(s)}"`;
}

const HEADER = `# Generated by mBot Studio
# mBot2 MicroPython Program
import cyberpi
import mbot2
import time
import mbuild

# Initialize
cyberpi.display.show_label("Running...", 16, "center", index=0)
`;

const FOOTER = `
# Program complete
cyberpi.display.show_label("Done! ✓", 16, "center", index=0)
mbot2.motor_stop("all")
`;

/**
 * Resolve a dc_motor_position block (percentage 0–100) to a concrete dc_motor command
 * using the robot config's action definitions.
 *
 * 0% = home state, 100% = opposite end.
 * Returns { port, speed, duration } or null if config is insufficient.
 *
 * @param {object} block - The dc_motor_position block { port, position }
 * @param {object} robotConfig - The robot-config.json object
 * @param {number|null} currentPercent - Current position (0–100), null means assume home (0%)
 */
export function resolveDcMotorPosition(block, robotConfig, currentPercent = null) {
  const port = block.port;
  const targetPct = Math.max(0, Math.min(100, Number(block.position ?? 0)));

  const hw = robotConfig?.additions?.find(a => a.port === port && a.type === 'dc_motor');
  if (!hw || !hw.actions || hw.actions.length < 2 || !hw.homeState) return null;

  // Identify the "away" action (goes from home → opposite end, i.e. 0% → 100%)
  const awayAction = hw.actions.find(a => a.targetState !== hw.homeState);
  // Identify the "home" action (goes from opposite end → home, i.e. 100% → 0%)
  const homeAction = hw.actions.find(a => a.targetState === hw.homeState);
  if (!awayAction || !homeAction) return null;

  const fullDuration = awayAction.duration || homeAction.duration || 1;
  const fromPct = typeof currentPercent === 'number' ? currentPercent : 0;
  const delta = targetPct - fromPct;

  if (Math.abs(delta) < 0.5) {
    return { port, speed: 0, duration: 0 };
  }

  // delta > 0 → moving toward 100% (away from home) → use awayAction direction
  // delta < 0 → moving toward 0% (back to home) → use homeAction direction
  const action = delta > 0 ? awayAction : homeAction;
  const dir = action.motorDirection || 'forward';
  const rawSpeed = action.speed || 50;
  const speed = dir === 'reverse' ? -rawSpeed : rawSpeed;
  const duration = Math.round((Math.abs(delta) / 100) * fullDuration * 100) / 100;

  return { port, speed, duration };
}

/**
 * Compute the state name for a given position percentage based on config.
 * Returns the endpoint state name if near 0% or 100%, otherwise a descriptive string.
 */
export function positionPercentToStateName(port, percent, robotConfig) {
  const hw = robotConfig?.additions?.find(a => a.port === port && a.type === 'dc_motor');
  if (!hw || !hw.homeState) return 'unknown';

  if (percent <= 0) return hw.homeState;

  const awayState = hw.actions?.find(a => a.targetState !== hw.homeState)?.targetState;
  if (percent >= 100) return awayState || 'unknown';

  return `${percent}% (between "${hw.homeState}" and "${awayState || '?'}")`;
}

/**
 * Convert a block program (JSON array) to MicroPython source code
 */
export function blocksToMicroPython(blocks, robotConfig = null) {
  let code = HEADER;

  // Add custom hardware setup if configured
  if (robotConfig?.additions) {
    code += '\n# Custom Hardware Setup\n';
    for (const hw of robotConfig.additions) {
      code += `# ${hw.label || hw.type}: Port ${hw.port}${hw.description ? ' - ' + hw.description : ''}\n`;
    }
    code += '\n';
  }

  code += '# --- Program Start ---\n';
  code += generateBlocksCode(blocks, 0, robotConfig);
  code += FOOTER;

  return code;
}

function indent(level) {
  return '    '.repeat(level);
}

/**
 * Evaluate a slot value (primitive or reporter block) into a Python expression.
 * - numbers → numeric literal
 * - identifier-looking strings → variable reference
 * - other strings → quoted string literal
 * - reporter blocks → recursive expression
 */
function evalExpr(v, fallback = 0) {
  if (v === undefined || v === null || v === '') return String(fallback);
  if (typeof v === 'number') return String(v);
  if (typeof v === 'boolean') return v ? '1' : '0';
  if (typeof v === 'object' && typeof v.type === 'string') return reporterExpr(v);
  const s = String(v);
  if (/^-?\d+(\.\d+)?$/.test(s)) return s;
  if (/^[a-zA-Z_][a-zA-Z0-9_]*$/.test(s)) return sanitizeIdentifier(s);
  return `"${escapePyString(s)}"`;
}

/** Number-only evaluation (used where the AI may emit a string-cast number). */
function evalNum(v, fallback = 0) {
  if (typeof v === 'object' && v && typeof v.type === 'string') return reporterExpr(v);
  if (v === undefined || v === null || v === '') return String(fallback);
  if (typeof v === 'number') return String(v);
  const s = String(v);
  if (/^-?\d+(\.\d+)?$/.test(s)) return s;
  if (/^[a-zA-Z_][a-zA-Z0-9_]*$/.test(s)) return sanitizeIdentifier(s);
  return String(fallback);
}

/** Boolean predicate evaluation (used by if_predicate / while_block / repeat_until). */
function evalBool(v, fallback = 'False') {
  if (typeof v === 'object' && v && typeof v.type === 'string') return `(${reporterExpr(v)})`;
  if (v === true) return 'True';
  if (v === false) return 'False';
  if (v === undefined || v === null || v === '') return fallback;
  return `bool(${evalExpr(v, 0)})`;
}

/** Convert a reporter block to a Python expression. */
function reporterExpr(b) {
  switch (b.type) {
    // numeric ops
    case 'op_add': return `(${evalExpr(b.a, 0)} + ${evalExpr(b.b, 0)})`;
    case 'op_sub': return `(${evalExpr(b.a, 0)} - ${evalExpr(b.b, 0)})`;
    case 'op_mul': return `(${evalExpr(b.a, 0)} * ${evalExpr(b.b, 0)})`;
    case 'op_div': return `(${evalExpr(b.a, 0)} / ${evalExpr(b.b, 1)})`;
    case 'op_mod': return `(${evalExpr(b.a, 0)} % ${evalExpr(b.b, 2)})`;
    case 'op_random':
      return `__import__('random').randint(${evalExpr(b.min, 1)}, ${evalExpr(b.max, 10)})`;
    case 'op_round': return `round(${evalExpr(b.a, 0)})`;
    case 'op_abs':   return `abs(${evalExpr(b.a, 0)})`;
    case 'op_function': {
      const fn = b.fn || 'sqrt';
      const a = evalExpr(b.a, 0);
      const fns = {
        floor: `int(${a})`,
        ceil:  `(int(${a}) + (1 if (${a}) > int(${a}) else 0))`,
        sqrt:  `((${a}) ** 0.5)`,
        sin:   `__import__('math').sin(${a})`,
        cos:   `__import__('math').cos(${a})`,
        tan:   `__import__('math').tan(${a})`,
        log:   `__import__('math').log(${a})`,
        neg:   `(-(${a}))`,
      };
      return fns[fn] || fns.sqrt;
    }
    // strings
    case 'op_join':   return `(str(${evalExpr(b.a, '""')}) + str(${evalExpr(b.b, '""')}))`;
    case 'op_letter': {
      const idx = `(int(${evalExpr(b.n, 1)}) - 1)`;
      const s = `str(${evalExpr(b.a, '""')})`;
      return `(${s}[${idx}] if 0 <= ${idx} < len(${s}) else "")`;
    }
    case 'op_length': return `len(str(${evalExpr(b.a, '""')}))`;

    // predicates
    case 'op_gt': return `(${evalExpr(b.a, 0)} > ${evalExpr(b.b, 0)})`;
    case 'op_lt': return `(${evalExpr(b.a, 0)} < ${evalExpr(b.b, 0)})`;
    case 'op_eq': return `(${evalExpr(b.a, 0)} == ${evalExpr(b.b, 0)})`;
    case 'op_and': return `(${evalBool(b.a, 'False')} and ${evalBool(b.b, 'False')})`;
    case 'op_or':  return `(${evalBool(b.a, 'False')} or ${evalBool(b.b, 'False')})`;
    case 'op_not': return `(not ${evalBool(b.a, 'False')})`;
    case 'op_contains':
      return `(str(${evalExpr(b.b, '""')}) in str(${evalExpr(b.a, '""')}))`;

    // sensor reporters
    case 'sensor_distance':   return 'mbuild.ultrasonic2.get()';
    case 'sensor_line':       return 'mbuild.dual_rgb_sensor.get_line_sta()';
    case 'sensor_brightness': return 'cyberpi.get_brightness()';
    case 'sensor_loudness':   return 'cyberpi.get_loudness()';
    case 'sensor_angle':
    case 'sensor_yaw':        return 'cyberpi.get_yaw()';
    case 'sensor_pitch':      return 'cyberpi.get_pitch()';
    case 'sensor_roll':       return 'cyberpi.get_roll()';
    case 'sensor_timer':      return 'cyberpi.timer.get()';
    case 'sensor_button_pressed':
      return `cyberpi.controller.is_press("${escapePyString(b.button || 'a')}")`;
    case 'sensor_obstacle_close':
      return `(mbuild.ultrasonic2.get() < ${evalExpr(b.distance, 20)})`;
    case 'sensor_is_shaking':
      return 'cyberpi.is_shake()';
    case 'sensor_is_upside_down':
      return '(cyberpi.get_roll() > 90 or cyberpi.get_roll() < -90)';
    case 'sensor_is_tilted': {
      const dir = b.direction || 'forward';
      // Threshold of ~30° tilt on the relevant axis
      const exprs = {
        forward:  '(cyberpi.get_pitch() > 30)',
        backward: '(cyberpi.get_pitch() < -30)',
        left:     '(cyberpi.get_roll() < -30)',
        right:    '(cyberpi.get_roll() > 30)',
      };
      return exprs[dir] || exprs.forward;
    }
    case 'sensor_battery':
      return 'cyberpi.get_battery()';
    case 'sensor_wifi_connected':
      return 'cyberpi.wifi.is_connect()';

    case 'var_get':
      return sanitizeIdentifier(b.name || 'my_var');

    default:
      return '0';
  }
}

function generateBlocksCode(blocks, level, robotConfig) {
  let code = '';

  for (const block of blocks) {
    code += generateBlockCode(block, level, robotConfig);
  }

  return code;
}

function generateBlockCode(block, level, robotConfig) {
  const pad = indent(level);

  switch (block.type) {
    // === Movement ===
    case 'move_forward':
      return `${pad}mbot2.forward(${evalNum(block.speed, 50)}, ${evalNum(block.duration, 1)})\n`;

    case 'move_backward':
      return `${pad}mbot2.backward(${evalNum(block.speed, 50)}, ${evalNum(block.duration, 1)})\n`;

    case 'turn_left':
      return `${pad}mbot2.turn(-(${evalNum(block.angle, 90)}))\n`;

    case 'turn_right':
      return `${pad}mbot2.turn(${evalNum(block.angle, 90)})\n`;

    case 'stop':
      return `${pad}mbot2.EM_stop()\n`;

    case 'set_speed': {
      const left = Number(block.left || 0);
      const right = Number(block.right || 0);
      const avg = Math.round((left + right) / 2);
      if (avg === 0) return `${pad}mbot2.EM_stop()\n`;
      if (avg > 0) return `${pad}mbot2.forward(${Math.abs(avg)})\n`;
      return `${pad}mbot2.backward(${Math.abs(avg)})\n`;
    }

    // === Sensors ===
    case 'if_obstacle': {
      let code = `${pad}if mbuild.ultrasonic2.get() < ${evalNum(block.distance, 20)}:\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1, robotConfig);
      }
      return code;
    }

    case 'if_line': {
      let code = `${pad}if mbuild.dual_rgb_sensor.is_line():\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1, robotConfig);
      }
      return code;
    }

    case 'if_color': {
      let code = `${pad}if mbuild.quad_rgb_sensor.is_color("${escapePyString(block.color || 'red')}"):\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1, robotConfig);
      }
      return code;
    }

    case 'get_distance':
      return `${pad}distance = mbuild.ultrasonic2.get()\n`;

    // === Sound & Display ===
    case 'play_tone':
      return `${pad}cyberpi.audio.play_tone(${evalNum(block.frequency, 440)}, ${evalNum(block.duration, 0.5)})\n`;

    case 'play_sound': {
      const sound = escapePyString(canonicalSound(block.sound || 'laugh'));
      return `${pad}cyberpi.audio.play("${sound}")\n`;
    }

    case 'play_melody': {
      const melody = escapePyString(block.melody || 'birthday');
      // Melodies are played as MIDI sequences on the firmware side via
      // cyberpi.audio.play_music(midi, beats). The preview shows a comment.
      return `${pad}# play melody: ${melody} (handled on robot)\n`;
    }

    case 'display_text':
      return `${pad}cyberpi.display.show_label("${escapePyString(block.text || 'Hello!')}", ${block.size || 16}, "center", index=0)\n`;

    case 'display_image': {
      const images = {
        happy: 'cyberpi.display.show_label("😊", 32, "center", index=0)',
        sad: 'cyberpi.display.show_label("😢", 32, "center", index=0)',
        heart: 'cyberpi.display.show_label("❤️", 32, "center", index=0)',
        star: 'cyberpi.display.show_label("⭐", 32, "center", index=0)',
        arrow_up: 'cyberpi.display.show_label("⬆️", 32, "center", index=0)',
        arrow_down: 'cyberpi.display.show_label("⬇️", 32, "center", index=0)',
      };
      return `${pad}${images[block.image] || images.happy}\n`;
    }

    case 'say':
      return `${pad}cyberpi.display.show_label("${escapePyString(block.text || '')}", 14, "center", index=0)\n`;

    // === Control Flow ===
    case 'wait':
      return `${pad}time.sleep(${evalNum(block.duration, 1)})\n`;

    case 'repeat': {
      let code = `${pad}for _i in range(int(${evalNum(block.times, 1)})):\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      return code;
    }

    case 'repeat_forever': {
      let code = `${pad}while True:\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      return code;
    }

    case 'repeat_until': {
      const cond = evalBool(block.cond, 'False');
      let code = `${pad}while not ${cond}:\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      code += `${indent(level + 1)}time.sleep(0.05)\n`;
      return code;
    }

    case 'while_block': {
      const cond = evalBool(block.cond, 'False');
      let code = `${pad}while ${cond}:\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      code += `${indent(level + 1)}time.sleep(0.05)\n`;
      return code;
    }

    case 'wait_until': {
      const cond = evalBool(block.cond, 'False');
      return `${pad}while not (${cond}):\n${indent(level + 1)}time.sleep(0.05)\n`;
    }

    case 'stop_all': {
      const what = block.what || 'all';
      if (what === 'this script') {
        return `${pad}return\n`;
      }
      return `${pad}mbot2.EM_stop()\n${pad}cyberpi.audio.stop()\n${pad}import sys\n${pad}sys.exit()\n`;
    }

    case 'if_predicate':
    case 'if_else_predicate': {
      const cond = evalBool(block.cond, 'False');
      let code = `${pad}if ${cond}:\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1, robotConfig);
      }
      return code;
    }

    case 'if_button': {
      let code = `${pad}if cyberpi.controller.is_press("${escapePyString(block.button || 'a')}"):\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      return code;
    }

    // === Advanced Control Flow ===
    case 'while_sensor': {
      const expr = sensorExpression(block.sensor || 'distance');
      const op = block.operator || '>';
      let condition;
      if (op === 'between') {
        condition = `${evalNum(block.min, 10)} <= ${expr} <= ${evalNum(block.max, 30)}`;
      } else {
        condition = `${expr} ${sanitizeOp(op)} ${evalNum(block.value, 20)}`;
      }
      let code = `${pad}while ${condition}:\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      code += `${indent(level + 1)}time.sleep(0.05)\n`;
      return code;
    }

    case 'move_until': {
      const dir = block.direction || 'forward';
      const spd = evalNum(block.speed, 50);
      const expr = sensorExpression(block.sensor || 'distance');
      const op = block.operator || '<';
      let condition;
      if (op === 'between') {
        condition = `not (${evalNum(block.min, 10)} <= ${expr} <= ${evalNum(block.max, 30)})`;
      } else {
        condition = `not (${expr} ${sanitizeOp(op)} ${evalNum(block.value, 20)})`;
      }
      let code = dir === 'backward'
        ? `${pad}mbot2.backward(abs(${spd}))\n`
        : `${pad}mbot2.forward(abs(${spd}))\n`;
      code += `${pad}while ${condition}:\n`;
      code += `${indent(level + 1)}time.sleep(0.05)\n`;
      code += `${pad}mbot2.EM_stop()\n`;
      return code;
    }

    // === Advanced Sensors ===
    case 'if_sensor_range': {
      const expr = sensorExpression(block.sensor || 'distance');
      let code = `${pad}if ${evalNum(block.min, 10)} <= ${expr} <= ${evalNum(block.max, 30)}:\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1, robotConfig);
      }
      return code;
    }

    case 'display_value': {
      const expr = sensorExpression(block.sensor || 'distance');
      const label = escapePyString(block.label || block.sensor || 'value');
      return `${pad}cyberpi.display.show_label("${label}: " + str(${expr}), 16, "center", index=0)\n`;
    }

    // === Variables & Math ===
    case 'set_variable': {
      const name = sanitizeIdentifier(block.name || 'my_var');
      if (block.source && block.source !== 'number') {
        const expr = sensorExpression(block.source);
        return `${pad}${name} = ${expr}\n`;
      }
      return `${pad}${name} = ${evalExpr(block.value, 0)}\n`;
    }

    case 'change_variable': {
      const name = sanitizeIdentifier(block.name || 'my_var');
      return `${pad}${name} = ${name} + ${evalExpr(block.by, 1)}\n`;
    }

    case 'math_operation': {
      const result = sanitizeIdentifier(block.result || 'result');
      const op = sanitizeOp(block.operator || '+');
      return `${pad}${result} = ${valueExpr(block.a)} ${op} ${valueExpr(block.b)}\n`;
    }

    // === Operators ===
    case 'random_number': {
      const result = sanitizeIdentifier(block.result || 'roll');
      const lo = Number(block.min ?? 1);
      const hi = Number(block.max ?? 10);
      return `${pad}import random as _random\n${pad}${result} = _random.randint(${lo}, ${hi})\n`;
    }

    case 'compare': {
      const result = sanitizeIdentifier(block.result || 'is_true');
      const op = sanitizeCompareOp(block.operator || '>');
      return `${pad}${result} = 1 if (${valueExpr(block.a)} ${op} ${valueExpr(block.b)}) else 0\n`;
    }

    case 'logic_op': {
      const result = sanitizeIdentifier(block.result || 'flag');
      const op = ['and', 'or', 'not'].includes(block.operator) ? block.operator : 'and';
      const a = valueExpr(block.a);
      if (op === 'not') return `${pad}${result} = 1 if (not ${a}) else 0\n`;
      const b = valueExpr(block.b);
      return `${pad}${result} = 1 if (${a} ${op} ${b}) else 0\n`;
    }

    case 'math_function': {
      const result = sanitizeIdentifier(block.result || 'answer');
      const fn = block.fn || 'abs';
      const a = valueExpr(block.a);
      const exprs = {
        abs:   `abs(${a})`,
        round: `round(${a})`,
        floor: `int(${a})`,
        ceil:  `(int(${a}) + (1 if (${a}) > int(${a}) else 0))`,
        sqrt:  `(${a}) ** 0.5`,
        sin:   `__import__('math').sin(${a})`,
        cos:   `__import__('math').cos(${a})`,
        tan:   `__import__('math').tan(${a})`,
        log:   `__import__('math').log(${a})`,
        neg:   `-(${a})`,
      };
      return `${pad}${result} = ${exprs[fn] || exprs.abs}\n`;
    }

    case 'mod': {
      const result = sanitizeIdentifier(block.result || 'rem');
      return `${pad}${result} = ${valueExpr(block.a)} % ${valueExpr(block.b)}\n`;
    }

    case 'string_join': {
      const result = sanitizeIdentifier(block.result || 'text');
      return `${pad}${result} = str(${valueExpr(block.a)}) + str(${valueExpr(block.b)})\n`;
    }

    case 'string_letter': {
      const result = sanitizeIdentifier(block.result || 'ch');
      const n = Number(block.n ?? 1);
      const idx = Math.max(1, n) - 1;
      return `${pad}_s = str(${valueExpr(block.a)})\n${pad}${result} = _s[${idx}] if len(_s) > ${idx} else ""\n`;
    }

    case 'string_length': {
      const result = sanitizeIdentifier(block.result || 'n');
      return `${pad}${result} = len(str(${valueExpr(block.a)}))\n`;
    }

    case 'string_contains': {
      const result = sanitizeIdentifier(block.result || 'found');
      return `${pad}${result} = 1 if (str(${valueExpr(block.b)}) in str(${valueExpr(block.a)})) else 0\n`;
    }

    // === Custom Hardware (uses starter_shield — verified working APIs) ===
    case 'dc_motor': {
      const port = block.port || 'M3';
      const pn = port === 'M1' ? 1 : port === 'M2' ? 2 : port === 'M3' ? 3 : 4;
      const speed = evalNum(block.speed, 50);
      const duration = evalNum(block.duration, 1);
      return `${pad}mbot2.starter_shield.dc_motor_set_power(${pn}, int(${speed}))\n${pad}time.sleep(${duration})\n${pad}mbot2.starter_shield.dc_motor_set_power(${pn}, 0)\n`;
    }

    case 'dc_motor_position': {
      // Percentage positioning — resolved to a dc_motor command using robotConfig.
      // The code generator resolves from assumed start (home = 0%) to target %.
      const resolved = resolveDcMotorPosition(block, robotConfig);
      if (!resolved) {
        return `${pad}# dc_motor_position: no config found for port ${String(block.port || '?').replace(/[\n\r]/g, '')}\n`;
      }
      if (resolved.duration <= 0) {
        return `${pad}# dc_motor_position: already at target position\n`;
      }
      const rpn = resolved.port === 'M1' ? 1 : resolved.port === 'M2' ? 2 : resolved.port === 'M3' ? 3 : 4;
      return `${pad}mbot2.starter_shield.dc_motor_set_power(${rpn}, ${resolved.speed})\n${pad}time.sleep(${resolved.duration})\n${pad}mbot2.starter_shield.dc_motor_set_power(${rpn}, 0)\n`;
    }

    case 'servo': {
      const port = block.port || 'S1';
      const pn = port === 'S1' ? 1 : port === 'S2' ? 2 : port === 'S3' ? 3 : 4;
      const angle = block.angle || 90;
      const speed = block.speed || 0;
      if (speed > 0) {
        const delay = ((101 - Math.max(1, Math.min(100, speed))) * 0.001).toFixed(3);
        return `${pad}_sv_cur = mbot2.starter_shield.servo_get_angle(${pn})\n${pad}if _sv_cur is None or _sv_cur < 0 or _sv_cur > 180:\n${pad}    _sv_cur = 90\n${pad}_sv_cur = int(_sv_cur)\n${pad}_sv_step = 1 if ${angle} > _sv_cur else -1\n${pad}while _sv_cur != ${angle}:\n${pad}    _sv_cur += _sv_step\n${pad}    mbot2.starter_shield.servo_set_angle(${pn}, _sv_cur)\n${pad}    time.sleep(${delay})\n`;
      }
      return `${pad}mbot2.starter_shield.servo_set_angle(${pn}, ${angle})\n`;
    }

    // === LEDs ===
    case 'set_led': {
      const color = block.color || 'green';
      if (color === 'off') {
        return `${pad}cyberpi.led.off()\n`;
      }
      const safeColor = escapePyString(color);
      return `${pad}cyberpi.led.show("${safeColor} ${safeColor} ${safeColor} ${safeColor} ${safeColor}")\n`;
    }

    case 'led_effect': {
      const effect = block.effect || 'rainbow';
      const effectMap = {
        rainbow: `${pad}cyberpi.led.rainbow_effect()\n`,
        breathe_red: `${pad}cyberpi.led.breathe("red")\n`,
        breathe_green: `${pad}cyberpi.led.breathe("green")\n`,
        breathe_blue: `${pad}cyberpi.led.breathe("blue")\n`,
      };
      return effectMap[effect] || effectMap.rainbow;
    }

    default:
      return `${pad}# Unknown block type: ${String(block.type).replace(/[\n\r]/g, ' ')}\n`;
  }
}

/**
 * Map sensor short-names to MicroPython expressions (using correct mbuild/cyberpi APIs)
 */
function sensorExpression(sensor) {
  const sensors = {
    distance: 'mbuild.ultrasonic2.get()',
    line: 'mbuild.dual_rgb_sensor.get_line_sta()',
    line_left: 'mbuild.dual_rgb_sensor.get_line_sta()',
    line_right: 'mbuild.dual_rgb_sensor.get_line_sta()',
    light: 'cyberpi.get_brightness()',
    brightness: 'cyberpi.get_brightness()',
    loudness: 'cyberpi.get_loudness()',
    angle: 'cyberpi.get_yaw()',
    yaw: 'cyberpi.get_yaw()',
    pitch: 'cyberpi.get_pitch()',
    roll: 'cyberpi.get_roll()',
    timer: 'cyberpi.timer.get()',
  };
  return sensors[sensor] || sensors.distance;
}

/**
 * Generate a single MQTT command from a block (for live mode)
 */
export function blockToMqttCommand(block) {
  return {
    type: block.type,
    params: { ...block },
    timestamp: Date.now(),
  };
}
