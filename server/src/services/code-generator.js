/**
 * Converts block programs (JSON) to MicroPython code for mBot2
 * This is used when generating code to upload to the robot
 */

const HEADER = `# Generated by mBot Studio
# mBot2 MicroPython Program
import cyberpi
import mbot2
import time
import mbuild

# Initialize
cyberpi.display.show_label("Running...", 16, "center", index=0)
`;

const FOOTER = `
# Program complete
cyberpi.display.show_label("Done! ✓", 16, "center", index=0)
mbot2.motor_stop("all")
`;

/**
 * Convert a block program (JSON array) to MicroPython source code
 */
export function blocksToMicroPython(blocks, robotConfig = null) {
  let code = HEADER;

  // Add custom hardware setup if configured
  if (robotConfig?.additions) {
    code += '\n# Custom Hardware Setup\n';
    for (const hw of robotConfig.additions) {
      code += `# ${hw.label || hw.type}: Port ${hw.port} - ${hw.description}\n`;
    }
    code += '\n';
  }

  code += '# --- Program Start ---\n';
  code += generateBlocksCode(blocks, 0);
  code += FOOTER;

  return code;
}

function indent(level) {
  return '    '.repeat(level);
}

function generateBlocksCode(blocks, level) {
  let code = '';

  for (const block of blocks) {
    code += generateBlockCode(block, level);
  }

  return code;
}

function generateBlockCode(block, level) {
  const pad = indent(level);

  switch (block.type) {
    // === Movement ===
    case 'move_forward':
      return `${pad}mbot2.forward(${block.speed || 50}, ${block.duration || 1})\n`;

    case 'move_backward':
      return `${pad}mbot2.backward(${block.speed || 50}, ${block.duration || 1})\n`;

    case 'turn_left':
      return `${pad}mbot2.turn_left(${block.speed || 50}, ${block.angle || 90})\n`;

    case 'turn_right':
      return `${pad}mbot2.turn_right(${block.speed || 50}, ${block.angle || 90})\n`;

    case 'stop':
      return `${pad}mbot2.motor_stop("all")\n`;

    case 'set_speed':
      return `${pad}mbot2.drive_speed(${block.left || 0}, ${block.right || 0})\n`;

    // === Sensors ===
    case 'if_obstacle': {
      let code = `${pad}if mbot2.ultrasonic2.get_distance() < ${block.distance || 20}:\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1);
      }
      return code;
    }

    case 'if_line': {
      const sensor = block.sensor || 'left';
      const isBlack = (block.color || 'black') === 'black';
      let sensorCode;
      if (sensor === 'both') {
        sensorCode = isBlack
          ? 'mbot2.line_follower.is_all_black()'
          : 'mbot2.line_follower.is_all_white()';
      } else {
        const sensorIdx = sensor === 'left' ? 1 : 2;
        sensorCode = isBlack
          ? `mbot2.line_follower.get_line(${sensorIdx}) < 50`
          : `mbot2.line_follower.get_line(${sensorIdx}) > 50`;
      }
      let code = `${pad}if ${sensorCode}:\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1);
      }
      return code;
    }

    case 'if_color': {
      let code = `${pad}if mbot2.color_sensor.get_color() == "${block.color || 'red'}":\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1);
      }
      return code;
    }

    case 'get_distance':
      return `${pad}distance = mbot2.ultrasonic2.get_distance()\n`;

    // === Sound & Display ===
    case 'play_tone':
      return `${pad}cyberpi.audio.play_tone(${block.frequency || 440}, ${block.duration || 0.5})\n`;

    case 'play_melody': {
      const melodies = {
        happy: 'cyberpi.audio.play_melody("birthday")',
        sad: 'cyberpi.audio.play_tone(200, 1)',
        excited: 'cyberpi.audio.play_melody("power_up")',
        alert: 'cyberpi.audio.play_melody("alert")',
      };
      return `${pad}${melodies[block.melody] || melodies.happy}\n`;
    }

    case 'display_text':
      return `${pad}cyberpi.display.show_label("${block.text || 'Hello!'}", ${block.size || 16}, "center", index=0)\n`;

    case 'display_image': {
      const images = {
        happy: 'cyberpi.display.show_label("😊", 32, "center", index=0)',
        sad: 'cyberpi.display.show_label("😢", 32, "center", index=0)',
        heart: 'cyberpi.display.show_label("❤️", 32, "center", index=0)',
        star: 'cyberpi.display.show_label("⭐", 32, "center", index=0)',
        arrow_up: 'cyberpi.display.show_label("⬆️", 32, "center", index=0)',
        arrow_down: 'cyberpi.display.show_label("⬇️", 32, "center", index=0)',
      };
      return `${pad}${images[block.image] || images.happy}\n`;
    }

    case 'say':
      return `${pad}cyberpi.display.show_label("${block.text || ''}", 14, "center", index=0)\n`;

    // === Control Flow ===
    case 'wait':
      return `${pad}time.sleep(${block.duration || 1})\n`;

    case 'repeat': {
      let code = `${pad}for _i in range(${block.times || 1}):\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      return code;
    }

    case 'repeat_forever': {
      let code = `${pad}while True:\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      return code;
    }

    case 'if_button': {
      let code = `${pad}if cyberpi.controller.is_press("${block.button || 'a'}"):\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      return code;
    }

    // === Advanced Control Flow ===
    case 'while_sensor': {
      const expr = sensorExpression(block.sensor || 'distance');
      const op = block.operator || '>';
      let condition;
      if (op === 'between') {
        condition = `${block.min ?? 10} <= ${expr} <= ${block.max ?? 30}`;
      } else {
        condition = `${expr} ${op} ${block.value ?? 20}`;
      }
      let code = `${pad}while ${condition}:\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      code += `${indent(level + 1)}time.sleep(0.05)\n`;
      return code;
    }

    case 'move_until': {
      const dir = block.direction || 'forward';
      const spd = block.speed || 50;
      const expr = sensorExpression(block.sensor || 'distance');
      const op = block.operator || '<';
      let condition;
      if (op === 'between') {
        // Move UNTIL value enters the range → loop while OUTSIDE the range
        condition = `not (${block.min ?? 10} <= ${expr} <= ${block.max ?? 30})`;
      } else {
        // Move UNTIL condition is true → loop while condition is NOT true
        condition = `not (${expr} ${op} ${block.value ?? 20})`;
      }
      const driveSpd = dir === 'backward' ? -spd : spd;
      let code = `${pad}mbot2.drive_speed(${driveSpd}, ${driveSpd})\n`;
      code += `${pad}while ${condition}:\n`;
      code += `${indent(level + 1)}time.sleep(0.05)\n`;
      code += `${pad}mbot2.motor_stop("all")\n`;
      return code;
    }

    // === Advanced Sensors ===
    case 'if_sensor_range': {
      const expr = sensorExpression(block.sensor || 'distance');
      let code = `${pad}if ${block.min ?? 10} <= ${expr} <= ${block.max ?? 30}:\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1);
      }
      return code;
    }

    case 'display_value': {
      const expr = sensorExpression(block.sensor || 'distance');
      const label = block.label || block.sensor || 'value';
      return `${pad}cyberpi.display.show_label("${label}: " + str(${expr}), 16, "center", index=0)\n`;
    }

    // === Variables & Math ===
    case 'set_variable': {
      const name = block.name || 'my_var';
      if (block.source && block.source !== 'number') {
        const expr = sensorExpression(block.source);
        return `${pad}${name} = ${expr}\n`;
      }
      return `${pad}${name} = ${block.value ?? 0}\n`;
    }

    case 'change_variable': {
      const name = block.name || 'my_var';
      const by = block.by ?? 1;
      return `${pad}${name} = ${name} + ${by}\n`;
    }

    case 'math_operation': {
      const result = block.result || 'result';
      const a = block.a ?? 0;
      const b = block.b ?? 0;
      const op = block.operator || '+';
      // a and b can be variable names (strings) or numbers
      const aExpr = typeof a === 'string' ? a : a;
      const bExpr = typeof b === 'string' ? b : b;
      return `${pad}${result} = ${aExpr} ${op} ${bExpr}\n`;
    }

    // === Custom Hardware ===
    case 'dc_motor': {
      const port = block.port || 'M3';
      const speed = block.speed || 50;
      const duration = block.duration || 1;
      return `${pad}mbot2.dc_motor_set("${port}", ${speed})\n${pad}time.sleep(${duration})\n${pad}mbot2.dc_motor_set("${port}", 0)\n`;
    }

    case 'servo': {
      const port = block.port || 'S1';
      const angle = block.angle || 90;
      return `${pad}mbot2.servo_set("${port}", ${angle})\n`;
    }

    default:
      return `${pad}# Unknown block type: ${block.type}\n`;
  }
}

/**
 * Map sensor short-names to MicroPython expressions
 */
function sensorExpression(sensor) {
  const sensors = {
    distance: 'mbot2.ultrasonic2.get_distance()',
    line_left: 'mbot2.line_follower.get_line(1)',
    line_right: 'mbot2.line_follower.get_line(2)',
    light: 'cyberpi.get_lightness()',
    loudness: 'cyberpi.get_loudness()',
    timer: 'cyberpi.timer.get()',
  };
  return sensors[sensor] || sensors.distance;
}

/**
 * Generate a single MQTT command from a block (for live mode)
 */
export function blockToMqttCommand(block) {
  return {
    type: block.type,
    params: { ...block },
    timestamp: Date.now(),
  };
}
