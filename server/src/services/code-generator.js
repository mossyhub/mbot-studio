/**
 * Converts block programs (JSON) to MicroPython code for mBot2
 * This is used when generating code to upload to the robot
 */

function escapePyString(value) {
  return String(value ?? '').replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n').replace(/\r/g, '\\r');
}

function sanitizeIdentifier(name) {
  const s = String(name ?? 'my_var').replace(/[^a-zA-Z0-9_]/g, '_');
  return /^[a-zA-Z_]/.test(s) ? s : '_' + s;
}

const ALLOWED_OPS = new Set(['>', '<', '>=', '<=', '==', '!=', '+', '-', '*', '/']);

function sanitizeOp(op) {
  return ALLOWED_OPS.has(op) ? op : '==';
}

const HEADER = `# Generated by mBot Studio
# mBot2 MicroPython Program
import cyberpi
import mbot2
import time
import mbuild

# Initialize
cyberpi.display.show_label("Running...", 16, "center", index=0)
`;

const FOOTER = `
# Program complete
cyberpi.display.show_label("Done! ✓", 16, "center", index=0)
mbot2.motor_stop("all")
`;

/**
 * Resolve a dc_motor_position block (percentage 0–100) to a concrete dc_motor command
 * using the robot config's action definitions.
 *
 * 0% = home state, 100% = opposite end.
 * Returns { port, speed, duration } or null if config is insufficient.
 *
 * @param {object} block - The dc_motor_position block { port, position }
 * @param {object} robotConfig - The robot-config.json object
 * @param {number|null} currentPercent - Current position (0–100), null means assume home (0%)
 */
export function resolveDcMotorPosition(block, robotConfig, currentPercent = null) {
  const port = block.port;
  const targetPct = Math.max(0, Math.min(100, Number(block.position ?? 0)));

  const hw = robotConfig?.additions?.find(a => a.port === port && a.type === 'dc_motor');
  if (!hw || !hw.actions || hw.actions.length < 2 || !hw.homeState) return null;

  // Identify the "away" action (goes from home → opposite end, i.e. 0% → 100%)
  const awayAction = hw.actions.find(a => a.targetState !== hw.homeState);
  // Identify the "home" action (goes from opposite end → home, i.e. 100% → 0%)
  const homeAction = hw.actions.find(a => a.targetState === hw.homeState);
  if (!awayAction || !homeAction) return null;

  const fullDuration = awayAction.duration || homeAction.duration || 1;
  const fromPct = typeof currentPercent === 'number' ? currentPercent : 0;
  const delta = targetPct - fromPct;

  if (Math.abs(delta) < 0.5) {
    return { port, speed: 0, duration: 0 };
  }

  // delta > 0 → moving toward 100% (away from home) → use awayAction direction
  // delta < 0 → moving toward 0% (back to home) → use homeAction direction
  const action = delta > 0 ? awayAction : homeAction;
  const dir = action.motorDirection || 'forward';
  const rawSpeed = action.speed || 50;
  const speed = dir === 'reverse' ? -rawSpeed : rawSpeed;
  const duration = Math.round((Math.abs(delta) / 100) * fullDuration * 100) / 100;

  return { port, speed, duration };
}

/**
 * Compute the state name for a given position percentage based on config.
 * Returns the endpoint state name if near 0% or 100%, otherwise a descriptive string.
 */
export function positionPercentToStateName(port, percent, robotConfig) {
  const hw = robotConfig?.additions?.find(a => a.port === port && a.type === 'dc_motor');
  if (!hw || !hw.homeState) return 'unknown';

  if (percent <= 0) return hw.homeState;

  const awayState = hw.actions?.find(a => a.targetState !== hw.homeState)?.targetState;
  if (percent >= 100) return awayState || 'unknown';

  return `${percent}% (between "${hw.homeState}" and "${awayState || '?'}")`;
}

/**
 * Convert a block program (JSON array) to MicroPython source code
 */
export function blocksToMicroPython(blocks, robotConfig = null) {
  let code = HEADER;

  // Add custom hardware setup if configured
  if (robotConfig?.additions) {
    code += '\n# Custom Hardware Setup\n';
    for (const hw of robotConfig.additions) {
      code += `# ${hw.label || hw.type}: Port ${hw.port}${hw.description ? ' - ' + hw.description : ''}\n`;
    }
    code += '\n';
  }

  code += '# --- Program Start ---\n';
  code += generateBlocksCode(blocks, 0, robotConfig);
  code += FOOTER;

  return code;
}

function indent(level) {
  return '    '.repeat(level);
}

function generateBlocksCode(blocks, level, robotConfig) {
  let code = '';

  for (const block of blocks) {
    code += generateBlockCode(block, level, robotConfig);
  }

  return code;
}

function generateBlockCode(block, level, robotConfig) {
  const pad = indent(level);

  switch (block.type) {
    // === Movement ===
    case 'move_forward':
      return `${pad}mbot2.forward(${block.speed || 50}, ${block.duration || 1})\n`;

    case 'move_backward':
      return `${pad}mbot2.backward(${block.speed || 50}, ${block.duration || 1})\n`;

    case 'turn_left':
      return `${pad}mbot2.turn(-${block.angle || 90})\n`;

    case 'turn_right':
      return `${pad}mbot2.turn(${block.angle || 90})\n`;

    case 'stop':
      return `${pad}mbot2.EM_stop()\n`;

    case 'set_speed': {
      const left = Number(block.left || 0);
      const right = Number(block.right || 0);
      const avg = Math.round((left + right) / 2);
      if (avg === 0) return `${pad}mbot2.EM_stop()\n`;
      if (avg > 0) return `${pad}mbot2.forward(${Math.abs(avg)})\n`;
      return `${pad}mbot2.backward(${Math.abs(avg)})\n`;
    }

    // === Sensors ===
    case 'if_obstacle': {
      let code = `${pad}if mbuild.ultrasonic2.get() < ${block.distance || 20}:\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1, robotConfig);
      }
      return code;
    }

    case 'if_line': {
      let code = `${pad}if mbuild.dual_rgb_sensor.is_line():\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1, robotConfig);
      }
      return code;
    }

    case 'if_color': {
      let code = `${pad}if mbuild.quad_rgb_sensor.is_color("${escapePyString(block.color || 'red')}"):\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1, robotConfig);
      }
      return code;
    }

    case 'get_distance':
      return `${pad}distance = mbuild.ultrasonic2.get()\n`;

    // === Sound & Display ===
    case 'play_tone':
      return `${pad}cyberpi.audio.play_tone(${block.frequency || 440}, ${block.duration || 0.5})\n`;

    case 'play_sound': {
      const sound = escapePyString(block.sound || 'laugh');
      return `${pad}cyberpi.audio.play("${sound}")\n`;
    }

    case 'play_melody': {
      const melody = escapePyString(block.melody || 'birthday');
      return `${pad}cyberpi.audio.play_melody("${melody}")\n`;
    }

    case 'display_text':
      return `${pad}cyberpi.display.show_label("${escapePyString(block.text || 'Hello!')}", ${block.size || 16}, "center", index=0)\n`;

    case 'display_image': {
      const images = {
        happy: 'cyberpi.display.show_label("😊", 32, "center", index=0)',
        sad: 'cyberpi.display.show_label("😢", 32, "center", index=0)',
        heart: 'cyberpi.display.show_label("❤️", 32, "center", index=0)',
        star: 'cyberpi.display.show_label("⭐", 32, "center", index=0)',
        arrow_up: 'cyberpi.display.show_label("⬆️", 32, "center", index=0)',
        arrow_down: 'cyberpi.display.show_label("⬇️", 32, "center", index=0)',
      };
      return `${pad}${images[block.image] || images.happy}\n`;
    }

    case 'say':
      return `${pad}cyberpi.display.show_label("${escapePyString(block.text || '')}", 14, "center", index=0)\n`;

    // === Control Flow ===
    case 'wait':
      return `${pad}time.sleep(${block.duration || 1})\n`;

    case 'repeat': {
      let code = `${pad}for _i in range(${block.times || 1}):\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      return code;
    }

    case 'repeat_forever': {
      let code = `${pad}while True:\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      return code;
    }

    case 'if_button': {
      let code = `${pad}if cyberpi.controller.is_press("${escapePyString(block.button || 'a')}"):\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      return code;
    }

    // === Advanced Control Flow ===
    case 'while_sensor': {
      const expr = sensorExpression(block.sensor || 'distance');
      const op = block.operator || '>';
      let condition;
      if (op === 'between') {
        condition = `${Number(block.min ?? 10)} <= ${expr} <= ${Number(block.max ?? 30)}`;
      } else {
        condition = `${expr} ${sanitizeOp(op)} ${Number(block.value ?? 20)}`;
      }
      let code = `${pad}while ${condition}:\n`;
      if (block.do && block.do.length > 0) {
        code += generateBlocksCode(block.do, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      code += `${indent(level + 1)}time.sleep(0.05)\n`;
      return code;
    }

    case 'move_until': {
      const dir = block.direction || 'forward';
      const spd = block.speed || 50;
      const expr = sensorExpression(block.sensor || 'distance');
      const op = block.operator || '<';
      let condition;
      if (op === 'between') {
        condition = `not (${Number(block.min ?? 10)} <= ${expr} <= ${Number(block.max ?? 30)})`;
      } else {
        condition = `not (${expr} ${sanitizeOp(op)} ${Number(block.value ?? 20)})`;
      }
      const driveSpd = dir === 'backward' ? -spd : spd;
      let code = `${pad}mbot2.forward(${Math.abs(driveSpd)})\n`;
      if (driveSpd < 0) code = `${pad}mbot2.backward(${Math.abs(driveSpd)})\n`;
      code += `${pad}while ${condition}:\n`;
      code += `${indent(level + 1)}time.sleep(0.05)\n`;
      code += `${pad}mbot2.EM_stop()\n`;
      return code;
    }

    // === Advanced Sensors ===
    case 'if_sensor_range': {
      const expr = sensorExpression(block.sensor || 'distance');
      let code = `${pad}if ${Number(block.min ?? 10)} <= ${expr} <= ${Number(block.max ?? 30)}:\n`;
      if (block.then && block.then.length > 0) {
        code += generateBlocksCode(block.then, level + 1, robotConfig);
      } else {
        code += `${indent(level + 1)}pass\n`;
      }
      if (block.else && block.else.length > 0) {
        code += `${pad}else:\n`;
        code += generateBlocksCode(block.else, level + 1, robotConfig);
      }
      return code;
    }

    case 'display_value': {
      const expr = sensorExpression(block.sensor || 'distance');
      const label = escapePyString(block.label || block.sensor || 'value');
      return `${pad}cyberpi.display.show_label("${label}: " + str(${expr}), 16, "center", index=0)\n`;
    }

    // === Variables & Math ===
    case 'set_variable': {
      const name = sanitizeIdentifier(block.name || 'my_var');
      if (block.source && block.source !== 'number') {
        const expr = sensorExpression(block.source);
        return `${pad}${name} = ${expr}\n`;
      }
      return `${pad}${name} = ${Number(block.value ?? 0)}\n`;
    }

    case 'change_variable': {
      const name = sanitizeIdentifier(block.name || 'my_var');
      const by = Number(block.by ?? 1);
      return `${pad}${name} = ${name} + ${by}\n`;
    }

    case 'math_operation': {
      const result = sanitizeIdentifier(block.result || 'result');
      const a = block.a ?? 0;
      const b = block.b ?? 0;
      const op = sanitizeOp(block.operator || '+');
      const aExpr = typeof a === 'string' ? sanitizeIdentifier(a) : Number(a);
      const bExpr = typeof b === 'string' ? sanitizeIdentifier(b) : Number(b);
      return `${pad}${result} = ${aExpr} ${op} ${bExpr}\n`;
    }

    // === Custom Hardware (uses starter_shield — verified working APIs) ===
    case 'dc_motor': {
      const port = block.port || 'M3';
      const pn = port === 'M1' ? 1 : port === 'M2' ? 2 : port === 'M3' ? 3 : 4;
      const speed = block.speed || 50;
      const duration = block.duration || 1;
      return `${pad}mbot2.starter_shield.dc_motor_set_power(${pn}, ${speed})\n${pad}time.sleep(${duration})\n${pad}mbot2.starter_shield.dc_motor_set_power(${pn}, 0)\n`;
    }

    case 'dc_motor_position': {
      // Percentage positioning — resolved to a dc_motor command using robotConfig.
      // The code generator resolves from assumed start (home = 0%) to target %.
      const resolved = resolveDcMotorPosition(block, robotConfig);
      if (!resolved) {
        return `${pad}# dc_motor_position: no config found for port ${String(block.port || '?').replace(/[\n\r]/g, '')}\n`;
      }
      if (resolved.duration <= 0) {
        return `${pad}# dc_motor_position: already at target position\n`;
      }
      const rpn = resolved.port === 'M1' ? 1 : resolved.port === 'M2' ? 2 : resolved.port === 'M3' ? 3 : 4;
      return `${pad}mbot2.starter_shield.dc_motor_set_power(${rpn}, ${resolved.speed})\n${pad}time.sleep(${resolved.duration})\n${pad}mbot2.starter_shield.dc_motor_set_power(${rpn}, 0)\n`;
    }

    case 'servo': {
      const port = block.port || 'S1';
      const pn = port === 'S1' ? 1 : port === 'S2' ? 2 : port === 'S3' ? 3 : 4;
      const angle = block.angle || 90;
      const speed = block.speed || 0;
      if (speed > 0) {
        const delay = ((101 - Math.max(1, Math.min(100, speed))) * 0.001).toFixed(3);
        return `${pad}_sv_cur = mbot2.starter_shield.servo_get_angle(${pn})\n${pad}if _sv_cur is None or _sv_cur < 0 or _sv_cur > 180:\n${pad}    _sv_cur = 90\n${pad}_sv_cur = int(_sv_cur)\n${pad}_sv_step = 1 if ${angle} > _sv_cur else -1\n${pad}while _sv_cur != ${angle}:\n${pad}    _sv_cur += _sv_step\n${pad}    mbot2.starter_shield.servo_set_angle(${pn}, _sv_cur)\n${pad}    time.sleep(${delay})\n`;
      }
      return `${pad}mbot2.starter_shield.servo_set_angle(${pn}, ${angle})\n`;
    }

    // === LEDs ===
    case 'set_led': {
      const color = block.color || 'green';
      if (color === 'off') {
        return `${pad}cyberpi.led.off()\n`;
      }
      const safeColor = escapePyString(color);
      return `${pad}cyberpi.led.show("${safeColor} ${safeColor} ${safeColor} ${safeColor} ${safeColor}")\n`;
    }

    case 'led_effect': {
      const effect = block.effect || 'rainbow';
      const effectMap = {
        rainbow: `${pad}cyberpi.led.rainbow_effect()\n`,
        breathe_red: `${pad}cyberpi.led.breathe("red")\n`,
        breathe_green: `${pad}cyberpi.led.breathe("green")\n`,
        breathe_blue: `${pad}cyberpi.led.breathe("blue")\n`,
      };
      return effectMap[effect] || effectMap.rainbow;
    }

    default:
      return `${pad}# Unknown block type: ${String(block.type).replace(/[\n\r]/g, ' ')}\n`;
  }
}

/**
 * Map sensor short-names to MicroPython expressions (using correct mbuild/cyberpi APIs)
 */
function sensorExpression(sensor) {
  const sensors = {
    distance: 'mbuild.ultrasonic2.get()',
    line: 'mbuild.dual_rgb_sensor.get_line_sta()',
    line_left: 'mbuild.dual_rgb_sensor.get_line_sta()',
    line_right: 'mbuild.dual_rgb_sensor.get_line_sta()',
    light: 'cyberpi.get_brightness()',
    brightness: 'cyberpi.get_brightness()',
    loudness: 'cyberpi.get_loudness()',
    angle: 'cyberpi.get_yaw()',
    yaw: 'cyberpi.get_yaw()',
    pitch: 'cyberpi.get_pitch()',
    roll: 'cyberpi.get_roll()',
    timer: 'cyberpi.timer.get()',
  };
  return sensors[sensor] || sensors.distance;
}

/**
 * Generate a single MQTT command from a block (for live mode)
 */
export function blockToMqttCommand(block) {
  return {
    type: block.type,
    params: { ...block },
    timestamp: Date.now(),
  };
}
